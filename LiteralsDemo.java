public class LiteralsDemo{
    public static void main(String[] args){
        // Integer Literals
        int a = 10;
        // Binary Literals
        int b = 0b10;
        // Octa Literals
        // int c = 010;
        int c = 075110;
        // Hexa Literals
        // int d = 0x10;
        int d = 0xe123f56;

        System.out.print(a+"_______"+b+"_______"+c+"_______"+"_______"+d);
    }
}