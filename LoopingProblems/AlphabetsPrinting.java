// Write a program to print all alphabets from a to z

import java.util.Scanner;

class AlphabetsPrinting {
    public static void main(String[] args) {
        System.out.println("All alphabets from a to z : ");
        for (char i = 'a'; i <= 'z'; i++) {
            System.out.print(i + " ");
        }
    }
}